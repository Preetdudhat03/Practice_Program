class Addition
{
	public static void main(String args[])
	{
		int a=5;
		int b=6;
		int c= a+b;
		System.out.println(c);

		byte p=5,q=6;
		System.out.println(p&q);
		System.out.println(p|q);
		System.out.println(p>>1);
		System.out.println(p<<1);
		System.out.println(p>>>1);
		System.out.println(~p);
		System.out.println(~q);

	}
}